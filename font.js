export function textToTextMap(text, spacing = 1) {
  const upperText = text.toUpperCase();
  const height = 7;
  const result = Array.from({ length: height }, () => "");

  for (let char of upperText) {
    const fontChar = pixelFont7x7[char] || pixelFont7x7[" "];
    for (let row = 0; row < height; row++) {
      result[row] += fontChar[row] + " ".repeat(spacing);
    }
  }

  return result;
}
